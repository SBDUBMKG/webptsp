      <h2><span>PRIVASI</span></h2>
      <li><b>Pengumpulan Informasi</b></li>
      <p style="text-align:justify">
        Kami wajib menjaga kerahasiaan informasi Pengguna, namun kewajiban tersebut dapat hilang apabila Pengelola 
        Situs diharuskan oleh ketentuan hukum yang berlaku, perintah pengadilan, dan/atau perintah dari aparat/instansi 
        yang berwenang, untuk membuka informasi-informasi milik Pengguna tersebut.
      </p>
      <li><b>Penggunaan Cookie (Computer)</b> </li>
      <p style="text-align:justify">
        Kami menggunakan cookie dan teknologi pelacakan tergantung pada fitur yang ditawarkan. Teknologi pelacakan 
        dan cookie bermanfaat untuk mengumpulkan informasi seperti jenis browser dan system operasional. 
        Hal tersebut dibutuhkan untuk mendata jumlah pengunjung situs dan memahami cara pengunjung menggunakan situs. 
        Cookie juga dapat membantu menyesuaikan situs untuk pengunjung. Informasi pribadi tidak dapat dikumpulkan melalui cookie dan teknologi 
        pelacak lainnya. Namun, jika Anda sebelumnya memberikan informasi identitas pribadi, cookie dapat terhubung ke informasi tersebut.
      </p>
      <p style="text-align:justify">Anda dapat memilih untuk men-disable cookies melalui setelan pada browser Anda, atau melalui setting 
        pada program semacam Norton Internet Security atau yang lainnya. Namun demikian, hal ini dapat mempengaruhi pengalaman Anda 
        dalam berinteraksi dengan situs kami sebagaimana juga dengan situs lainnya. Termasuk Anda tidak bisa masuk ke layanan kami, 
        seperti login ke forum atau ke akun yang Anda miliki.
      </p>
      <li><b>File Log</b></li>
      <p style="text-align:justify">
        Seperti kebanyakan situs lain, kami mengumpulkan dan menggunakan data yang terdapat pada file log. 
        Informasi yang terdapat pada file log termasuk alamat IP (Internet Protocol) Anda, ISP (Internet Service Provider), 
        browser yang Anda gunakan, waktu pada saat Anda berkunjung dan halaman mana saja yang Anda buka selama berkunjung di Website Minerba . </p>
      <p style="text-align:justify">
        Website Minerba  dan vendor pihak ketiga bersama-sama menggunakan cookie pihak pertama (seperti cookie Google Analytics) 
        atau pengenal pihak pertama lainnya, dan cookie pihak ketiga (seperti cookie DoubleClick) atau pengenal pihak ketiga lainnya.
      </p>
      <p style="text-align:justify">
        Hal-hal mengenai kebijakan privasi yang belum ditulis akan kami tambahkan jika di suatu saat memang dirasa perlu untuk ditambahkan.
      </p>
